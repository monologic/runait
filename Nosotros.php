								<hr />

								<h4>Acerca de Nosotros</h4>
								<p>Runa IT nace de la necesidad de contar con aliado que acompañe en las actividades de la todas las personas de la sociedad. Así también acompañar en el desarrollo de las diferentes actividades y/o áreas dentro de la empresa, tales como las ventas, las operaciones, la logística, la gestión y la interacción entre cada una de estas áreas con la finalidad de lograr una sinergia y poder optimizar el funcionamiento de la empresa.</p>

								<p>Runa IT está totalmente comprometido con el medio ambiente, razón por la cual todos nuestros procesos se esfuerzan por minimizar los impactos negativos que puedan haber sobre nuestro entorno.</p>

								<p>La responsabilidad social es otro de los temas en los que Runa IT pone especial énfasis. La contribución que se pueda tener sobre un desarrollo sostenido de la sociedad, acompañando a las personas que no han tenido oportunidades para desplegar todo su potencial en nuestra comunidad.</p>
								<p>Runa IT, donde Runa (Quechua) = Gente y IT (Inglés) = Tecnologías de la información tal como su nombre lo indica, tiene como objetivo último, lograr que la gente mejore el estándar y calidad de vida haciendo uso de la tecnología. La innovación de productos y procesos que van a contribuir con esta finalidad, deben ser parte del resultado que se obtenga de las operaciones realidad en Runa IT.</p>

